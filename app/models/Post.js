function Post(title, subtitle, body, public, featured, archived, author, id, dataCreazione, image) {
    this.title = title;
    this.subtitle = subtitle;
    this.body = body;
    this.public = public;
    this.featured = featured;
    this.archived = archived;
    this.author = author;
    this.id = id;
    this.dataCreazione = dataCreazione;
    this.image = image;
}
